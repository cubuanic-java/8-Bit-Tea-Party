;Нуль символ не обязателен.

string:
text_length:    dw 0000h                ;Offset +0
text_offset:    dw 0000h                ;Where is text?
;Text = STRING ADDR + OFFSET;
reserved:       db 00, dw 0000h         ;align word
text            db 'Text'

